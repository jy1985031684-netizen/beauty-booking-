// YYYY-MM-DD 形式の日付が営業日（土日祝）かどうか
export function isBusinessDay(dateStr) {
  const date = new Date(dateStr + 'T00:00:00+09:00')
  const dow = date.getDay()
  return dow === 0 || dow === 6 || HOLIDAYS.has(dateStr)
}
